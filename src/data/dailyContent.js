import { WANTED_LIST } from './wantedList';

export const getDailyContent = () => {
    const today = new Date();
    const dayIndex = today.getDate() + today.getMonth();

    // Rotate items based on the day index
    const mainNewsIndex = dayIndex % NEWS_ARCHIVE.length;
    let sideNews1Index = (dayIndex + 1) % NEWS_ARCHIVE.length;
    let sideNews2Index = (dayIndex + 2) % NEWS_ARCHIVE.length;

    if (sideNews1Index === mainNewsIndex) sideNews1Index = (sideNews1Index + 1) % NEWS_ARCHIVE.length;
    if (sideNews2Index === mainNewsIndex || sideNews2Index === sideNews1Index) sideNews2Index = (sideNews2Index + 1) % NEWS_ARCHIVE.length;

    // Use WANTED_LIST for the daily criminal to ensure consistency
    const criminalIndex = dayIndex % WANTED_LIST.length;
    const selectedCriminal = WANTED_LIST[criminalIndex];

    const criminalData = {
        ...selectedCriminal,
        crime: selectedCriminal.reason // Map 'reason' to 'crime' for compatibility
    };

    return {
        main: NEWS_ARCHIVE[mainNewsIndex],
        side1: NEWS_ARCHIVE[sideNews1Index],
        side2: NEWS_ARCHIVE[sideNews2Index],
        criminal: criminalData,
        date: today.toLocaleDateString('ar-EG', { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' }),
        issueNo: 420 + dayIndex
    };
};
